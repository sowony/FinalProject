DROP TABLE CRWL;
DROP SEQUENCE CRWLSEQ;

CREATE SEQUENCE CRWLSEQ;
CREATE TABLE CRWL(
	WCRWLNO 		NUMBER 			PRIMARY KEY,
	WNO				NUMBER 			NOT NULL,
	WCRWLWRITER 	VARCHAR2(200)	NOT NULL,
	WCRWLFORM 		VARCHAR2(200) 	NOT NULL,
	WCRWLCONTENT 	VARCHAR2(2000)	NOT NULL,
	WCRWLDATE 		DATE 			NOT NULL,
	WCRWLLINK 		VARCHAR2(2000) 	NOT NULL
);

SELECT * FROM CRWL;